import Foundation

class SeluruhBahan {
    public var seluruh_bahan: [semua] = []
    public func addBahan(nama: String, berat: Double, harga: Double) {
        seluruh_bahan.append(semua(nama_bahan: nama, berat_bahan: berat, harga_modal: harga))
    }
    
}
